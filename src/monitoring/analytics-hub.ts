	private checkForAlerts(): void {
		if (!this.config.alerts.enabled) return;

		const metrics = this.getUnifiedMetrics();

		// Check various thresholds
		if (metrics.performanceMetrics.errorRate > this.config.alerts.thresholds.errorRate) {
			this.triggerAlert({
				type: 'performance',
				severity: 'high',
				message: `Error rate exceeded threshold: ${metrics.performanceMetrics.errorRate}`,
				metrics: { errorRate: metrics.performanceMetrics.errorRate },
			});
		}

		if (metrics.performanceMetrics.pageLoadTime > this.config.alerts.thresholds.responseTime) {
			this.triggerAlert({
				type: 'performance',
				severity: 'medium',
				message: `Response time exceeded threshold: ${metrics.performanceMetrics.pageLoadTime}ms`,
				metrics: { responseTime: metrics.performanceMetrics.pageLoadTime },
			});
		}

		if (metrics.userAnalytics.bounceRate > this.config.alerts.thresholds.abandonmentRate) {
			this.triggerAlert({
				type: 'usability',
				severity: 'medium',
				message: `Bounce rate exceeded threshold: ${metrics.userAnalytics.bounceRate}`,
				metrics: { bounceRate: metrics.userAnalytics.bounceRate },
			});
		}

		if (metrics.accessibilityComplianceScore < this.config.alerts.thresholds.accessibilityScore) {
			this.triggerAlert({
				type: 'accessibility',
				severity: 'high',
				message: `Accessibility score below threshold: ${metrics.accessibilityComplianceScore}`,
				metrics: { accessibilityScore: metrics.accessibilityComplianceScore },
			});
		}
	}

