	// Export all data
	public exportData(
		options: {
			format?: 'json' | 'csv' | 'parquet';
			includeRaw?: boolean;
			dateRange?: { start: Date; end: Date };
		} = {},
	): string {
		const { format = 'json', includeRaw = false, dateRange } = options;

		const data = {
			metadata: {
				exportedAt: new Date().toISOString(),
				format,
				dateRange,
				version: '1.0.0',
			},

			// Aggregated metrics
			unifiedMetrics: this.getUnifiedMetrics(),

			// Raw data if requested
			rawData: includeRaw
				? {
						userAnalytics: userAnalytics.exportAnalytics(),
						navigationData: navigationAnalysis.exportNavigationData(),
						featureData: featureUsageAnalytics.exportFeatureData(),
						realtimeData: realtimeInteractionTracker.getSessionRecording(),
						performanceData: performanceObserver.exportMetrics(),
						accessibilityData: accessibilityAudit.exportAuditResults(),
					}
				: undefined,

			// Configuration
			configuration: this.config,
		};

		switch (format) {
			case 'json':
				return JSON.stringify(data, null, 2);

			case 'csv':
				return this.convertToCSV(data);

			case 'parquet':
				return this.convertToParquet(data);

			default:
				return JSON.stringify(data, null, 2);
		}
	}

