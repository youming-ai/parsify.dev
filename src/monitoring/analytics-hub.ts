	private calculateUXImpact(recommendations: UnifiedRecommendation[]): number {
		const uxRecommendations = recommendations.filter((r) => r.category === 'ui');
		if (uxRecommendations.length === 0) return 0;

		return uxRecommendations.reduce((sum, r) => sum + r.expectedImpact.improvement, 0) / uxRecommendations.length;
	}

