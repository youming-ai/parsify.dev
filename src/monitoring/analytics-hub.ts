	private performHealthCheck(): void {
		const checks = [
			this.checkUserAnalyticsHealth(),
			this.checkNavigationAnalysisHealth(),
			this.checkFeatureAnalyticsHealth(),
			this.checkRealtimeTrackerHealth(),
			this.checkPerformanceObserverHealth(),
			this.checkAccessibilityAuditHealth(),
		];

		const failedChecks = checks.filter((check) => check.status === 'fail');
		const warnChecks = checks.filter((check) => check.status === 'warn');

		let status: 'healthy' | 'degraded' | 'unhealthy' = 'healthy';
		if (failedChecks.length > 0) {
			status = 'unhealthy';
		} else if (warnChecks.length > 0) {
			status = 'degraded';
		}

		this.healthCheck = {
			status,
			checks,
			uptime: Date.now() - (this.healthCheck.lastCheck.getTime() - this.healthCheck.uptime),
			version: '1.0.0',
			lastCheck: new Date(),
		};
	}

